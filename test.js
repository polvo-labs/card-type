/**
 * Module dependencies
 */

import test from 'ava'
import { cardType, cards } from '.'

/**
 * Helpers
 */

function check (values, expected, t) {
  values.forEach(value => {
    const result = cardType(value)
    t.is(result, expected, `${value} ${expected}`)
  })
}

